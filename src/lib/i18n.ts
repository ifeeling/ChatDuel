export function t(language: UserLanguage, key: string): string {
  return dict[language]?.[key] ?? (language === 'zh-CN' ? undefined : dict['en-US'][key]) ?? dict['zh-CN'][key] ?? key
}

export function isSupportedLanguage(value: unknown): value is UserLanguage {
  return typeof value === 'string' && (SUPPORTED_LANGUAGE_CODES as string[]).includes(value)
}

